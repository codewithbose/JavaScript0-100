// while loop
let i=0
while (i<=10) {
    console.log(`Value of i: ${i}`);
    i++
}

let myHeroes = ["Batman", "Ironman", "Thor", "Hulk", "Spiderman"]
let myArr = 0
while (myArr<myHeroes.length) {
    console.log(`My Hero is: ${myHeroes[myArr]}`);
    myArr++
}

// Do while loop
let score = 11
do {
    console.log(`My score: ${score}`);
    score++
} while (score<=10);